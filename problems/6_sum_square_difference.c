// 1 + ... + n = n(n+1)/2
// un = 1^2 + ... n^2
// un - un-1 = n^2
//
// n - (n-1) = 1
// n^2 - (n-1)^2 = n^2 - n^2 + 2n - 1 = 2n - 1
// n^3 - (n-1)^3 = -(-3n^2 + 3n - 1) = 3n^2 - 3n + 1
//
// un = 1 + ... + n
// un - un-1 = n = ((2n-1) + 1)/2
// so un = (n^2 + n)/2
//       = n(n+1)/2
//
// vn = 1^2 + ... + n^2
// vn - vn-1 = n^2 = (2(3n^2 - 3n + 1) + 3(2n-1) + 1)/6
// so vn = (2n^3 + 3n^2 + n)/6
//       = n(2n^2 + 3n +1)/6
//
// un**2 - vn = n^2(n+1)^2/4 - n(2n^2 + 3n + 1)/6
//            = n/12( 3n(n+1)^2 - 4n^2 - 6n - 2 )
//            = n/12( 3n^3 + 6n^2 + 3n - 4n^2 - 6n - 2)
//            = n/12( 3n^3 + 2n^2 -3n - 2)

int64_t problem6_sum_square_difference()
{
	constexpr int64_t n = 100;
	return n * (3 * n * n * n + 2 * n * n - 3 * n - 2) / 12;
}
